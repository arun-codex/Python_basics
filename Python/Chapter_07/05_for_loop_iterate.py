
# Using list to iterate
L = [1,2,3,4,5]

for i in L:
    print(i)

# Using tuple to iterate
t = (1,2,3,4,5)

for i in t:
    print(i)


# Using set to iterate
s = {"Arun","Deepak","Karan","Kavya","Khushi"}

for i in s:
    print(i)


# Using string to iterate
s1 = "Arun"

for i in s1:
    print(i)  